#amb int() passem a nombre enter el que l'usuari escriu.
x = int(input("Digues el valor de x: "))
y = int(input("Digues el valor de y: "))

#suma
resultat = x + y
print("La suma és:",resultat)

#resta
resultat = x - y
print("La resta de x menys y és:",resultat)

#multiplicació
resultat = x * y
print("La multiplicació és:",resultat)

#divisió
resultat = x / y
print("La divisió de x entre y:",resultat)

#mòdul
resultat = x % y
print("El mòdul de x entre y és:",resultat)

#Potència
resultat = x ** y
print("x elevat a y és:",resultat)
