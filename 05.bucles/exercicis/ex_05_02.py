#tenim una variable que val 0
x = 0

# BUCLE WHILE
while x < 100: #mentre la x és més petita de 100..
  x += 2 #Augmentem la x en 2
  print (x) #imprimim el valor de x


print ("Ara tornem al 0...")

while x != 0: #mentre la x no és 0
  x -= 2 #disminuim la x en 2
  print (x) #imprimim el valor de x
